// Operator

// 1. Operator Aritmatika
let a = 2, b = 5, c = "10"
console.log("a + b = " + (a+b));
console.log("a + c = " + (a+c));
console.log("a - b = " + (a-b));
console.log("a * b = " + (a*b));
console.log("b / a = " + (b/a));
console.log("b % a = " + (b%a));
console.log("b ** a = " + (b**a));

// 2. Operator Penugasan
let x = 4, y = 2
console.log("x = y -> " + (x=y));
console.log("x += y -> " + (x+=y));
console.log("x -= y -> " + (x-=y));
console.log("x *= y -> " + (x*=y));
console.log("x /= y -> " + (x/=y));
console.log("x %= y -> " + (x%=y));
console.log("x **= y -> " + (x**=y));

// 3. Operator Perbandingan
let d = 5, e = 10, f = "5"
